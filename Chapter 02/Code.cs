// Define and assign values to the variables
int x = 16;
int y = 4;
// This is ‘+’ operation int add = x + y;
Console.WriteLine($"{x} + {y} = {x + y}");
// This is ‘-’ operation int subtract = x - y;
Console.WriteLine($"{x} - {y} = {x - y}");
// This is ‘*’ operation int multiply = x * y;
Console.WriteLine($"{x} * {y} = {x * y}");
// This is ‘/’ operation int divide = x / y;
Console.WriteLine($"{x} / {y} = {x / y}");
// This is ‘%’ operation int modulus = x % y;
Console.WriteLine($"{x} % {y} = {x % y}");

